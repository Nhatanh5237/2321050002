let banner = document.getElementsByClassName("img")[0];
let tenPhim = document.getElementsByClassName("ten-phim")[0];
let nam = document.getElementsByClassName("nam")[0];
let tuoi = document.getElementsByClassName("tuoi")[0];
let thoiLuong = document.getElementsByClassName("thoi-luong")[0];
let quocGia = document.getElementsByClassName("quoc-gia")[0];
// getElementsByClassName() trả về một danh sách, nên lấy phần tử [0].
// Mỗi biến tương ứng với một phần tử HTML trong giao diện để thay đổi nội dung khi chọn phim mới:
// banner → ảnh dán
// tenPhim → text tên phim
// nam → năm phát hành...

function chonPhim(idPhim) {
  for (let i = 0; i < danhSachPhim.length; i++) {
    if (danhSachPhim[i].id == idPhim) {
      // Đổi banner
      banner.src = danhSachPhim[i].banner;

      // Cập nhật phim hiện tại
      phimHienTai = danhSachPhim[i];

      tenPhim.innerHTML = danhSachPhim[i].tenPhim;
      nam.innerHTML = " • " + danhSachPhim[i].namPhatHanh;
      tuoi.innerHTML = " • T" + danhSachPhim[i].tuoi;
      thoiLuong.innerHTML = " • " + danhSachPhim[i].thoiLuong + " giờ";
      quocGia.innerHTML = " • Quốc gia: " + danhSachPhim[i].quocGia;
    }
  }
}
// Hàm chạy qua tất cả các phim trong danh sách.
// Tìm phim có id == idPhim được truyền vào.
// Khi tìm đúng phim, nó cập nhật giao diện

function playTrailer() {
  if (!phimHienTai || !phimHienTai.trailer) {
    alert("Phim này chưa có trailer!");
    return;
  }

  window.open(phimHienTai.trailer, "_blank");
}

